public class LadderIfElseEx2{
public static void main(String[]args){
int score=665 ;

if(score<=500){
System.out.println("Poor Score");
}
else if(score>=650){
System.out.println("Average Score");
}
else if(score>=750){
System.out.println("Good Score");
}
else{
System.out.println("Excellent Score");
}

}
}